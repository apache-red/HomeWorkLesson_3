public class Circle {


    private double X;
    private double Y;
    private double R;


    public Circle(double x, double y, double r) {
        X = x;
        Y = y;
        R = r;
    }

    public double getX() {
        return X;
    }

    public double getY() {
        return Y;
    }

    public double getR() {
        return R;
    }

    public void setX(double x) {
        X = x;
    }

    public void setY(double y) {
        Y = y;
    }

    public void setR(double r) {
        R = r;
    }









}